export const getFeaturedProducts = async (req, res) => {
	try {
		console.log("Fetching featured products...");
		let featuredProducts;

		try {
			// Try to get from Redis first
			console.log("Checking Redis cache...");
			const cachedProducts = await redis.get("featured_products");
			if (cachedProducts) {
				console.log("Serving from Redis cache");
				return res.json(JSON.parse(cachedProducts));
			}
		} catch (redisError) {
			console.error("Redis error:", redisError);
		}

		try {
			// If not in redis or redis fails, fetch from MongoDB
			console.log("Fetching from MongoDB...");
			featuredProducts = await Product.find({ isFeatured: true }).lean();
			console.log("MongoDB query successful, found", featuredProducts.length, "products");

			if (!featuredProducts || featuredProducts.length === 0) {
				console.log("No featured products found in database");
				return res.status(404).json({ message: "No featured products found" });
			}

			// Try to cache in Redis, but don't fail if it doesn't work
			try {
				console.log("Caching in Redis...");
				await redis.set("featured_products", JSON.stringify(featuredProducts), 'EX', 3600);
				console.log("Successfully cached in Redis");
			} catch (cacheError) {
				console.error("Failed to update Redis cache:", cacheError);
			}

			res.json(featuredProducts);
		} catch (dbError) {
			console.error("Database error:", dbError);
			throw dbError;
		}
	} catch (error) {
		console.error("Error in getFeaturedProducts controller:", error);
		res.status(500).json({ 
			message: "Server error", 
			error: process.env.NODE_ENV === 'production' ? undefined : error.message,
			stack: process.env.NODE_ENV === 'development' ? error.stack : undefined
		});
	}
};

export const createProduct = async (req, res) => {
	try {
		const { name, description, price, image, category } = req.body;

		let cloudinaryResponse = null;

		if (image) {
			cloudinaryResponse = await cloudinary.uploader.upload(image, { folder: "products" });
		}

		const product = await Product.create({
			name,
			description,
			price,
			image: cloudinaryResponse?.secure_url ? cloudinaryResponse.secure_url : "",
			category,
		});

		res.status(201).json(product);
	} catch (error) {
		console.log("Error in createProduct controller", error.message);
		res.status(500).json({ message: "Server error", error: error.message });
	}
};

export const deleteProduct = async (req, res) => {
	try {
		const product = await Product.findById(req.params.id);

		if (!product) {
			return res.status(404).json({ message: "Product not found" });
		}

		if (product.image) {
			const publicId = product.image.split("/").pop().split(".")[0];
			try {
				await cloudinary.uploader.destroy(`products/${publicId}`);
				console.log("deleted image from cloduinary");
			} catch (error) {
				console.log("error deleting image from cloduinary", error);
			}
		}

		await Product.findByIdAndDelete(req.params.id);

		res.json({ message: "Product deleted successfully" });
	} catch (error) {
		console.log("Error in deleteProduct controller", error.message);
		res.status(500).json({ message: "Server error", error: error.message });
	}
};

export const getRecommendedProducts = async (req, res) => {
	try {
		const products = await Product.aggregate([
			{
				$sample: { size: 4 },
			},
			{
				$project: {
					_id: 1,
					name: 1,
					description: 1,
					image: 1,
					price: 1,
				},
			},
		]);

		res.json(products);
	} catch (error) {
		console.log("Error in getRecommendedProducts controller", error.message);
		res.status(500).json({ message: "Server error", error: error.message });
	}
};

